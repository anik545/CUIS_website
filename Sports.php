<h3>Cricket Match Against CU Pakistan Society 2015</h3>
<strong>When</strong>: Saturday, 26th November<br>
<strong>Time</strong>: 10:00am - 1:00pm
<p>On the morning of the 26th November India Soc did battle against  Pakistan Soc in 2 indoor cricket matches. After a professional warm up,  India was bowling first. Excellent bowling performances from all the  bowlers meant regular wickets were taken and the scoring rate was low.  Left with only 79 to win, Prashant Chand-Bajpai made short work of the  chase and we won with 8 wickets to spare!</p>
<p>The second match was much closer. We batted first but lost wickets  regularly and were reduced to 65 off our allotted overs. With such a  small total (especially for indoor matches) nothing but an exceptional  bowling and fielding effort would do. This is exactly what we produced  and amazingly, reduced Pakistan to about 40!</p>
<p>A massive WELL PLAYED to those who represented India Soc, and we hope  you all enjoyed it. Pak Soc have asked for a rematch which we will aim  to play next term. Hope you're all looking forward to it!</p>
