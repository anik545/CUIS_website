<h3>T20 Cricket Against CU Sri Lanka Soc 2011</h3>
<p> <img src="Images/Sports/Sports_2020.jpg" alt="20/20 Cricket Match" id="noborder" align="left" height="242">On Sunday 19th June 2011 our very own India Soc took on Sri Lanka Soc in a 20/20 cricket match at Fitzwilliam Sports Grounds. We won the toss and elected to bat first, hoping to put their bowlers under serious pressure. Adarsh Bala and Akshay Deshmukh put on a huge partnership, with Akshay ultimately notching up a blistering century. As the ball continuously disappeared out of the ground we felt confident and in the end India Soc posted 159 off our 20 overs.</p>
<p>We started bowling well, and would have had an early wicket; however a controversial decision by their umpire meant that the initial breakthrough was denied. Their batsmen batted well and put away the loose deliveries and despite tidy spells from Debuyan Dasgupta, and Neeru Ravi, Sri Lanka Soc eventually chased down our score.</p>
<p>A massive thanks to all who played and whatever the result, we hope everyone had a great time.</p>
