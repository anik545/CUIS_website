<h3>Indian Republic Day Celebrations</h3>
<blockquote>
  <p><strong>When: </strong>Sunday 30th January 2011<strong><br>
    Time: </strong>4pm - 7pm <strong><br>
      Where: </strong>Mong Hall, Sidney Sussex College</p>
  <p> Republic day marks the transition of India from a British dominion to an independent republic with its own constitution on the 26th January 1950. It is one of the three national holidays in India and while the main parade, Republic Day Parade, takes place at the Rajpath, in the national capital New Delhi, where the President views the parade, state capitals also have their state celebrations. On Sunday 30th January, we too will be celebrating this momentous day in our own way!</p>
  <p>The renowned Dr Gordon Johnson will be giving a short talk as part of our celebration. He is a prominent British historian of colonial India and his main interests are the history of India since the late eighteenth century and the more recent history of the University of Cambridge. Among his many other activities are editor of the "New Cambridge History of India", and President of Wolfson College, Cambridge until  2010. His publications include "The Cultural Atlas of India" and "University Politics: F.M.Cornford's Cambridge".<br>
